  private void AgregarTabla(){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Id");
        dtm.addColumn("Nombre");
        dtm.addColumn("Apellido Paterno");
        dtm.addColumn("Apellido Materno");
        dtm.addColumn("Correo");
        dtm.addColumn("Num Tel");
        
        Object[] fila = new Object[6];
        controladores.VendedoresJpaController cp = new controladores.VendedoresJpaController();
        List<Vendedores> lista = cp.findVendedoresEntities();
        for(Vendedores p:lista){
            fila[0] = p.getId();
            fila[1] = p.getNombre();
            fila[2] = p.getAp();
            fila[3] = p.getAm();
            fila[4] = p.getCorreo();
            fila[5] = p.getNum();
            dtm.addRow(fila);
        }
        tblInventario.setModel(dtm);
    }
