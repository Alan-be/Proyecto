    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Vendedores inv = new Vendedores();
        inv.setNombre(txtNombre.getText());
        inv.setAp(txtAP.getText());
        inv.setAm(txtAM.getText());
        inv.setCorreo(txtCorreo.getText());
        inv.setNum(Integer.parseInt(txtNum.getText()));
        controladores.VendedoresJpaController ic = new controladores.VendedoresJpaController();
        ic.create(inv);
        JOptionPane.showMessageDialog(this, "Producto agregado");
        AgregarTabla();
        Limpiar();

    }//GEN-LAST:event_btnRegistrarActionPerformed

      private void Limpiar(){
        txtNombre.setText(" ");
        txtAP.setText("");
        txtAM.setText("");
        txtCorreo.setText("");
        txtNum.setText("");
    }
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int renglon = tblInventario.getSelectedRow();
        int id = (int)tblInventario.getValueAt(renglon, 0);
        Vendedores inv = new Vendedores();
        inv.setId(id);
        inv.setNombre(txtNombre.getText());
        inv.setAp(txtAP.getText());
        inv.setAm(txtAM.getText());
        inv.setCorreo(txtCorreo.getText());
        inv.setNum(Integer.parseInt(txtNum.getText()));
        controladores.VendedoresJpaController ic = new controladores.VendedoresJpaController();
        try {
            ic.edit(inv);
            AgregarTabla();
            Limpiar();
        } catch (Exception ex) {
            Logger.getLogger(CapturaInventario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int renglon = tblInventario.getSelectedRow();
        int id = (int)tblInventario.getValueAt(renglon, 0);
        controladores.VendedoresJpaController elim = new controladores.VendedoresJpaController();
        try {
            elim.destroy(id);
            Limpiar();
            AgregarTabla();
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(CapturaInventario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

