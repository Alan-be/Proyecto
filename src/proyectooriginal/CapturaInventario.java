    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Inventario inv = new Inventario();
        inv.setProducto(txtProducto.getText());
        inv.setPrecioCompra(Float.parseFloat(txtPC.getText()));
        inv.setPrecioVenta(Float.parseFloat(txtPV.getText()));
        int cant = (int) spinnerCantidad.getValue();
        inv.setCantidad(cant);
        controladores.InventarioJpaController ic = new controladores.InventarioJpaController();
        ic.create(inv);
        JOptionPane.showMessageDialog(this, "Producto agregado");
        AgregarTabla();
        Limpiar();
        
        
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed
   private void Limpiar(){
        txtProducto.setText(" ");
        txtPC.setText("");
        txtPV.setText("");
        spinnerCantidad.setValue(0);
    }
            
            
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int renglon = tblInventario.getSelectedRow(); 
        int id = (int)tblInventario.getValueAt(renglon, 0);
        Inventario inv = new Inventario();
        inv.setId(id);
        inv.setProducto(txtProducto.getText());
        inv.setPrecioCompra(Float.parseFloat(txtPC.getText()));
        inv.setPrecioVenta(Float.parseFloat(txtPV.getText()));
        int cant = (int) spinnerCantidad.getValue();
        inv.setCantidad(cant);
        InventarioJpaController ic = new InventarioJpaController();
        try {
            ic.edit(inv);
            AgregarTabla();
            Limpiar();
        } catch (Exception ex) {
            Logger.getLogger(CapturaInventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int renglon = tblInventario.getSelectedRow();
        int id = (int)tblInventario.getValueAt(renglon, 0);
        InventarioJpaController elim = new InventarioJpaController();
        try {
            elim.destroy(id);
            Limpiar();
            AgregarTabla();
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(CapturaInventario.class.getName()).log(Level.SEVERE, null, ex);
        }        
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

