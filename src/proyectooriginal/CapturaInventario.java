    private void AgregarTabla(){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Id");
        dtm.addColumn("Producto");
        dtm.addColumn("PC");
        dtm.addColumn("PV");
        dtm.addColumn("Cantidad");
        
        Object[] fila = new Object[5];
        InventarioJpaController cp = new InventarioJpaController();
        List<Inventario> lista = cp.findInventarioEntities();
        for(Inventario p:lista){
            fila[0] = p.getId();
            fila[1] = p.getProducto();
            fila[2] = p.getPrecioCompra();
            fila[3] = p.getPrecioVenta();
            fila[4] = p.getCantidad();
            dtm.addRow(fila);
        }
        tblInventario.setModel(dtm);
    }
